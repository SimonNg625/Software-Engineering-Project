package sportapp.model;

/**
 * Represents the category of equipment in the sport management system.
 * <p>
 * This enum defines whether equipment is sellable or borrowable.
 */
public enum EquipmentCategory {

    /**
     * Equipment that can be sold.
     */
    SELLABLE,

    /**
     * Equipment that can be borrowed.
     */
    BORROWABLE
}
